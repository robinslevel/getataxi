function applyLanguage(lang) {
  const dict = translations[lang] || translations.nl;
  document.documentElement.lang = lang;

  document.querySelectorAll("[data-i18n]").forEach((el) => {
    const key = el.getAttribute("data-i18n");
    if (dict[key]) el.textContent = dict[key];
  });

  localStorage.setItem("taxipoint_lang", lang);

  document.querySelectorAll(".lang__btn").forEach((btn) => {
    btn.classList.toggle("is-active", btn.dataset.lang === lang);
  });
}

document.querySelectorAll(".lang__btn").forEach((btn) => {
  btn.addEventListener("click", () => applyLanguage(btn.dataset.lang));
});

const savedLang = localStorage.getItem("taxipoint_lang") || "nl";
applyLanguage(savedLang);

// Mobile menu
const burger = document.getElementById("burger");
const nav = document.getElementById("nav");

if (burger && nav) {
  burger.addEventListener("click", () => {
    const isOpen = nav.classList.toggle("is-open");
    burger.setAttribute("aria-expanded", String(isOpen));
  });

  nav.querySelectorAll("a").forEach((link) => {
    link.addEventListener("click", () => {
      nav.classList.remove("is-open");
      burger.setAttribute("aria-expanded", "false");
    });
  });
}

const yearEl = document.getElementById("year");
if (yearEl) yearEl.textContent = new Date().getFullYear();

// Preselect car
const carSelect = document.getElementById("carSelect");
document.querySelectorAll("[data-car]").forEach((btn) => {
  btn.addEventListener("click", () => {
    const car = btn.getAttribute("data-car");
    if (carSelect) carSelect.value = car;
  });
});

